/**
 * sync-fifa-teams.mjs
 * Fetches the 48 WC 2026 teams + groups from football-data.org (competition 2000).
 * FIFA rankings are NOT available from this API; they are kept from the last known values.
 *
 * Usage (from web/ directory):
 *   FOOTBALL_DATA_API_TOKEN=<token> node scripts/sync-fifa-teams.mjs
 *
 * The script ONLY updates group assignments. Rankings require a separate source.
 */

import { writeFile, readFile } from "node:fs/promises";
import { fileURLToPath } from "node:url";
import path from "node:path";

const token = process.env.FOOTBALL_DATA_API_TOKEN;
if (!token) {
  console.error("Set FOOTBALL_DATA_API_TOKEN env var");
  process.exit(1);
}

const here = path.dirname(fileURLToPath(import.meta.url));
const outPath = path.join(here, "..", "lib", "teams.ts");

async function fetchJSON(url) {
  const res = await fetch(url, { headers: { "X-Auth-Token": token } });
  if (!res.ok) throw new Error(`${res.status} ${url}`);
  return res.json();
}

async function main() {
  const [teamsData, standingsData] = await Promise.all([
    fetchJSON("https://api.football-data.org/v4/competitions/2000/teams"),
    fetchJSON("https://api.football-data.org/v4/competitions/2000/standings"),
  ]);

  // Build group map: tla -> single letter (A-L)
  const groupByTla = {};
  for (const standing of standingsData.standings ?? []) {
    const letter = standing.group?.replace("GROUP_", "") ?? "";
    for (const entry of standing.table ?? []) {
      if (letter) groupByTla[entry.team.tla] = letter;
    }
  }

  // Read the existing teams.ts to preserve FIFA rankings
  let existingTs = "";
  try { existingTs = await readFile(outPath, "utf8"); } catch {}
  const rankMap = {};
  for (const m of existingTs.matchAll(/code:\s*"(\w+)",\s*name:\s*"[^"]+",\s*rank:\s*(\d+)/g)) {
    rankMap[m[1]] = Number(m[2]);
  }

  // TLA (football-data 3-letter) → FIFA lowercase code mapping
  // Most are just .toLowerCase(), but a few differ
  const tlaToFifa = {
    URY: "uru", GER: "ger", ESP: "esp", PAR: "par", ARG: "arg",
    GHA: "gha", BRA: "bra", POR: "por", JPN: "jpn", MEX: "mex",
    ENG: "eng", USA: "usa", KOR: "kor", FRA: "fra", RSA: "rsa",
    ALG: "alg", AUS: "aus", NZL: "nzl", SUI: "sui", ECU: "ecu",
    SWE: "swe", CZE: "cze", CRO: "cro", KSA: "ksa", TUN: "tun",
    TUR: "tur", SEN: "sen", BEL: "bel", MAR: "mar", AUT: "aut",
    COL: "col", EGY: "egy", CAN: "can", HAI: "hai", IRN: "irn",
    BIH: "bih", PAN: "pan", CPV: "cpv", COD: "cod", CIV: "civ",
    QAT: "qat", JOR: "jor", IRQ: "irq", UZB: "uzb", NED: "ned",
    NOR: "nor", SCO: "sco", CUW: "cuw",
  };

  const nameOverrides = {
    "South Korea": "Korea Republic",
    "Bosnia-Herzegovina": "Bosnia & Herz.",
    "Cape Verde Islands": "Cabo Verde",
    "Ivory Coast": "Côte d'Ivoire",
    "United States": "USA",
    "Iran": "IR Iran",
    "Congo DR": "Congo DR",
  };

  const teams = [];
  for (const t of teamsData.teams ?? []) {
    const code = tlaToFifa[t.tla] ?? t.tla.toLowerCase();
    const group = groupByTla[t.tla] ?? "?";
    const name = nameOverrides[t.name] ?? t.name;
    const rank = rankMap[code] ?? 999;
    teams.push({ code, name, rank, group });
  }

  teams.sort((a, b) => a.rank - b.rank || a.name.localeCompare(b.name));

  const lines = teams.map((t) => {
    const safeName = t.name.replaceAll("\\", "\\\\").replaceAll('"', '\\"');
    return `  { code: "${t.code}", name: "${safeName}", rank: ${t.rank}, group: "${t.group}" },`;
  });

  const out =
    `// THIS FILE IS AUTO-GENERATED by scripts/sync-fifa-teams.mjs\n` +
    `// Source: football-data.org competition 2000 (FIFA World Cup 2026)\n` +
    `// FIFA rankings preserved from last known values — update manually if needed\n\n` +
    `export type Team = {\n  code: string;\n  name: string;\n  rank: number;\n  group: string;\n};\n\n` +
    `export const TEAMS: Team[] = [\n${lines.join("\n")}\n];\n`;

  await writeFile(outPath, out, "utf8");
  console.log(JSON.stringify({ wrote: outPath, count: teams.length, unknownRanks: teams.filter(t => t.rank === 999).map(t => t.code) }, null, 2));
}

main().catch((e) => { console.error(e); process.exit(1); });

const pageUrl = "https://www.fifa.com/en/tournaments/mens/worldcup/canadamexicousa2026/teams";

import { writeFile } from "node:fs/promises";
import { fileURLToPath } from "node:url";
import path from "node:path";

function htmlDecode(text) {
  return text
    .replaceAll("&amp;", "&")
    .replaceAll("&quot;", '"')
    .replaceAll("&#39;", "'")
    .replaceAll("&lt;", "<")
    .replaceAll("&gt;", ">")
    .replaceAll("&nbsp;", " ")
    .trim();
}

function normalizeWhitespace(text) {
  return String(text).replace(/\s+/g, " ").trim();
}

async function fetchPrerenderedHtml(url) {
  const res = await fetch(url, {
    headers: {
      "user-agent": "Googlebot/2.1 (+http://www.google.com/bot.html)",
      "accept-language": "en-US,en;q=0.9",
    },
  });

  if (!res.ok) {
    throw new Error(`Fetch failed ${res.status} for ${url}`);
  }

  return res.text();
}

function extractTeamsFromHtml(html) {
  // The prerendered HTML contains repeated blocks like:
  // <div class="... team-card_teamCard__...">
  //   ... flags-sq-4/ALG ...
  //   <h3 class="team-card_teamName__..."> ... <span>Algeria</span> ...
  //   ... World Ranking ... <span>28</span>
  //
  // We'll split on the stable class prefix and parse each chunk.
  const marker = "team-card_teamCard__";
  const parts = html.split(marker);
  const chunks = parts.slice(1).map((p) => p.slice(0, 12000)); // cap chunk length

  const teams = [];
  const seen = new Set();

  for (const chunk of chunks) {
    const fifaCodeMatch = chunk.match(/flags-sq-\d+\/([A-Z]{3})/);
    const nameMatch = chunk.match(
      /team-card_teamName__[^\"]+\">[\s\S]*?<span[^>]*>([^<]+)<\/span>/,
    );
    const rankingMatch = chunk.match(/World Ranking<\/span><span[^>]*>(\d+)<\/span>/);

    // The prerendered HTML contains a row like:
    //   >Stage</span><span ...>Group B</span>
    const groupMatch = chunk.match(/>Stage<\/span><span[^>]*>Group\s+([A-Z])<\/span>/);

    if (!fifaCodeMatch || !nameMatch || !rankingMatch || !groupMatch) continue;

    const code = fifaCodeMatch[1].toLowerCase();
    const name = normalizeWhitespace(htmlDecode(nameMatch[1]));
    const ranking = Number(rankingMatch[1]);
    const group = String(groupMatch[1]);

    if (!Number.isFinite(ranking) || ranking <= 0) continue;

    const key = `${code}:${name}`;
    if (seen.has(key)) continue;
    seen.add(key);

    teams.push({ code, name, rank: ranking, group });
  }

  // De-dupe by code (prefer the first name we saw).
  const byCode = new Map();
  for (const t of teams) {
    if (!byCode.has(t.code)) byCode.set(t.code, t);
  }

  return [...byCode.values()];
}

function toTeamsTs(teams) {
  const header = `// THIS FILE IS AUTO-GENERATED by scripts/sync-fifa-teams.mjs\n`;

  const escapedLines = teams
    .slice()
    .sort((a, b) => a.rank - b.rank || a.name.localeCompare(b.name))
    .map((t) => {
      const safeName = t.name.replaceAll("\\", "\\\\").replaceAll("\"", "\\\"");
      return `  { code: "${t.code}", name: "${safeName}", rank: ${t.rank}, group: "${t.group}" },`;
    });

  return (
    header +
    `\nexport type Team = {\n  code: string;\n  name: string;\n  rank: number;\n  group: string;\n};\n\n` +
    `export const TEAMS: Team[] = [\n${escapedLines.join("\n")}\n];\n`
  );
}

async function main() {
  const html = await fetchPrerenderedHtml(pageUrl);
  const teams = extractTeamsFromHtml(html);

  if (teams.length < 10) {
    throw new Error(`Unexpectedly small team count (${teams.length}). FIFA HTML likely changed.`);
  }

  const here = path.dirname(fileURLToPath(import.meta.url));
  const outPath = path.join(here, "..", "lib", "teams.ts");

  const out = toTeamsTs(teams);
  await writeFile(outPath, out, "utf8");
  console.log(JSON.stringify({ wrote: outPath, count: teams.length, sample: teams.slice(0, 5) }, null, 2));
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});
